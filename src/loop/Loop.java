
package loop;

public class Loop {

    public static void main(String[] args) {
        
        int kVal = 8;
        int kv = 1;
        
        //While loop
        while(kVal > 1)
        {
            kv *= kVal;
            kVal -= 1;
        }
        System.out.println(kv);
        
        //do-while loop
        
    }
    
}
